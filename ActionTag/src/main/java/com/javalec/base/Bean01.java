package com.javalec.base;

public class Bean01 {
	//	Field
	private String name;
	private int value;
	//	Constructor
	public Bean01() {
		
	}
	//	Method
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	

}
